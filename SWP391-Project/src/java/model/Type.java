package model;

/**
 *
 * @author ADMIN
 */
public class Type {
    private int typeId;
    private String type, img;

    public Type() {
    }

    public Type(int typeId, String type, String img) {
        this.typeId = typeId;
        this.type = type;
        this.img = img;
    }

    public int getTypeId() {
        return typeId;
    }

    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
    
}
